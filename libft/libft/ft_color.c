#include <unistd.h>

static void	ft_putchar(char c)
{
	write(1, &c, 1);
}

static void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void		ft_color(char *str)
{
	ft_putstr("\033[01;");
	ft_putstr(str);
	ft_putstr("m");
}
