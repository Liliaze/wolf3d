#include <unistd.h>

void	ft_putnbr(int n)
{
	long	i;
	char	c;
	long	sign;

	i = 1;
	sign = 1;
	c = '-';
	if (n < 0)
	{
		sign = -1;
		write(1, &c, 1);
	}
	while (i <= (n * sign))
		i *= 10;
	if (i >= 10)
		i /= 10;
	while (i > 0)
	{
		c = ((n * sign) / i) + '0';
		write(1, &c, 1);
		n = n % i;
		i /= 10;
	}
}
